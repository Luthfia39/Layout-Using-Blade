    <p>
        Berdasarkan Kamus Besar Bahasa Indonesia, bunga adalah bagian tumbuhan yang akan menjadi buah, 
        biasanya elok warnanya dan harum baunya; kembang. Dilansir dari 
        <span><a class="link" href="https://id.wikipedia.org/wiki/Bunga">id.wikipedia.org</a></span>, 
        Bunga atau kembang (kata serapan dari bahasa Jawa: ꦏꦼꦩ꧀ꦧꦁ, 
        translit. kêmbang; bahasa Latin: flos) adalah alat reproduksi seksual pada tumbuhan berbunga 
        (divisio Magnoliophyta atau Angiospermae, "tumbuhan berbiji tertutup"). Pada bunga terdapat 
        organ reproduksi, yaitu benang sari dan putik.<br><br>
        Bunga dapat muncul secara tunggal maupun bersama-sama dalam satu rangkaian. Bunga yang muncul 
        secara bersama-sama disebut sebagai bunga majemuk atau inflorescence. Pada beberapa spesies, 
        bunga majemuk dapat dianggap awam sebagai bunga (tunggal), misalnya pada Anthurium dan bunga matahari. 
        Satuan bunga yang menyusun bunga majemuk disebut floret.<br><br>
        Secara botani, bunga adalah bagian tanaman untuk menghasilkan biji. Penyerbukan dan pembuahan 
        berlangsung pada bunga. Setelah pembuahan, bunga akan berkembang lebih lanjut membentuk buah. 
        Pada tumbuhan berbunga, buah adalah struktur yang membawa dan melindungi biji.
